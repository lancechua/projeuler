	def collatz(x):
		col_seq = [x]
		while True:
			if x == 1:
				break
			elif x % 2 == 0:
				x = int(x / 2)
			else:
				x = int(3 * x + 1)
			col_seq.append(x)
		return col_seq
	col_list = pool.map( lambda x: len(collatz(x)) , range(1,n+1))

	return col_list.index(max(col_list)) + 1

