def prob19(start='1 Jan 1901', end='31 Dec 2000'):
  '''Counting Sundays'''
  date0 = parse(start)
  dateN = parse(end)  

  res = 0
  dateI = date0
  while dateI < dateN:
    if dateI.weekday() == 6 and dateI.day == 1:
      res += 1
    dateI += timedelta(days=1)

  return res

