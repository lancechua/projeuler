def is_prime(n):
	'''reference : https://en.wikipedia.org/wiki/Primality_test'''
	if n <= 1:
		return False
	elif n <= 3:
		return True
	elif n % 2 == 0 or n % 3 == 0:
		return False
	i = 5
	while i ** 2 <= n:
		if n % i == 0 or n % (i + 2) == 0:
			return False
		i+=6
	return True


def num2dig(y):
	'''returns input number as a list of its digits'''
	return [int(mpm.fmod(y, 10**(a))/ (10 ** (a-1))) for a in range(int(mpm.ceil(mpm.log(y,10))),0,-1)]

def get_divs(n):
	divs = []
	for a in range(1,int(n**(1/2)) + 1):
		if n % a == 0:
			if n / a == a:
				divs+= [int(a)]
			else:
				divs+= [int(a), int(n / a)]
	return divs

