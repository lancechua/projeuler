def prob3(x=600851475143):
	'''Largest prime factor'''
	n = int(x ** 1 / 2) + 1
	b = x
	for a in range(2, n):
		if b <= a:
			return b
			break
		while True:
			if b % a == 0:
				b /= a
			else:
				break
	return b

def prob4(x=999):
	'''Largest palindrome product'''
	def is_palindrome(num):
		num_str = str(int(num))
		return num_str == num_str[::-1]

	cur_res = 0
	for a in range(x,1,-1):
		for b in range(a,1,-1):
			if is_palindrome(a * b):
				cur_res = max(a * b , cur_res)
	return cur_res


def prob5(x=20):
	'''Smallest multiple'''
	primes = [2,3,5,7,11,13,17,19]
	df = pd.DataFrame(index=primes, columns=[(a+1) for a in range(x)])
	df.fillna(0, inplace=True)

	for a in range(x):
		cur_div = a + 1        
		for b in primes:
			while True:
				if cur_div % b == 0:
					df.loc[b, a + 1] += 1
					cur_div /= b
				else:
					break
	df = df.max(axis=1)
	return functools.reduce(operator.mul, [ a ** df[a] for a in df.index ], 1)

